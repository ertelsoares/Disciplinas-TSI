package equation;

public class SecondDegreeEquation {
	private float a;
	private float b;
	private float c;

	public SecondDegreeEquation(float a, float b, float c) {
       this.a = a;
       this.b = b;
       this.c = c;
	}

	public float getA() {
		return a;
	}

	public float getB() {
		return b;
	}

	public float getC() {
		return c;
	}
}
